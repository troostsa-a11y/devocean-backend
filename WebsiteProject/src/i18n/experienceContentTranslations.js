// Helper function to get translated content for a specific experience and language
export function getExperienceContent(experienceKey, lang = 'en') {
  // Normalize language codes
  const normalizedLang = lang || 'en';
  
  // Get the experience translations
  const experience = EXPERIENCE_CONTENT_TRANSLATIONS[experienceKey];
  if (!experience) {
    console.warn(`No translations found for experience: ${experienceKey}`);
    return null;
  }
  
  // Try exact match first, then fallback to base language, then English
  let content = experience[normalizedLang] || 
                experience[normalizedLang.split('-')[0]] || 
                experience['en'];
  
  return content;
}
